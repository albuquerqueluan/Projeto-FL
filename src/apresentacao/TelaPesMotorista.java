    private void carregarTabela() {
        try {
            
            Vector<String> cabecalho = new Vector();
            cabecalho.add("CÃ³digo");
            cabecalho.add("CNH");
            cabecalho.add("Nome");
            cabecalho.add("Local");
            
            NMotorista negocio = new NMotorista();
            Vector linhas = new Vector();
            
            for (Motorista tipo : negocio.listar()) {
                
                Vector<String> detalhe = new Vector();
                
                detalhe.add(tipo.getIdentificador()+ "");
                detalhe.add(tipo.getCNH()+ "");
                detalhe.add(tipo.getNome()+ "");
                detalhe.add(tipo.getLocal()+ "");
                
                linhas.add(detalhe);
                
            }
            
            tblResultado.setModel(new DefaultTableModel(linhas, cabecalho));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }


